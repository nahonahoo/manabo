// NPCが購入するかどうかを判定
function checkNPCBuy(item) {
  // shopItemsから非売品以外を対象に
  const candidates = NPCS.filter(npc => {
    const catMatch = npc.likes.some(l => item.cat && item.cat.includes(l)) || npc.likes.includes(item.cat);
    const rarityMatch = npc.likeRarity.includes(item.rarity);
    const priceOk = item.price >= npc.budget[0] && item.price <= npc.budget[1];
    return catMatch && rarityMatch && priceOk;
  });
  if (candidates.length === 0) return null;
  return candidates[Math.floor(Math.random() * candidates.length)];
}

